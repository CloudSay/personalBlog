<template><h3 id="时间复杂度" tabindex="-1"><a class="header-anchor" href="#时间复杂度" aria-hidden="true">#</a> 时间复杂度</h3>
<p>一个算法的时间复杂度反映了程序运行从开始到结束所需要的时间。把算法中基本操作重复执行的次数（频度）作为算法的时间复杂度。</p>
<p>没有循环语句，记作<code>O(1)</code>，也称为常数阶。只有一重循环，则算法的基本操作的执行频度与问题规模n呈线性增大关系，记作<code>O（n）</code>，也叫线性阶。</p>
<p>常见的时间复杂度有：</p>
<ul>
<li><code>O(1)</code>: Constant Complexity: Constant 常数复杂度</li>
<li><code>O(log n)</code>: Logarithmic Complexity: 对数复杂度</li>
<li><code>O(n)</code>: Linear Complexity: 线性时间复杂度</li>
<li><code>O(n^2)</code>: N square Complexity 平⽅方</li>
<li><code>O(n^3)</code>: N square Complexity ⽴立⽅方</li>
<li><code>O(2^n)</code>: Exponential Growth 指数</li>
<li><code>O(n!)</code>: Factorial 阶乘</li>
</ul>
